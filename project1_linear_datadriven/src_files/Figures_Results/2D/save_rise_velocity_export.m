function save_rise_velocity_export(Re, Bo, time_nd, export_data, metadata)
%SAVE_RISE_VELOCITY_EXPORT Save analysis-ready rise-velocity arrays for manuscript review.

    rmsd_percent = struct();
    dns_centroid = export_data.centroid.dns;
    dns_mean = export_data.mean.dns;

    rmsd_percent.centroid = struct( ...
        'e_pod', compute_rmsd_percent(dns_centroid, export_data.centroid.e_pod), ...
        'm_pod', compute_rmsd_percent(dns_centroid, export_data.centroid.m_pod), ...
        'l_pod', compute_rmsd_percent(dns_centroid, export_data.centroid.l_pod), ...
        'e_dmd', compute_rmsd_percent(dns_centroid, export_data.centroid.e_dmd), ...
        'm_dmd', compute_rmsd_percent(dns_centroid, export_data.centroid.m_dmd), ...
        'l_dmd', compute_rmsd_percent(dns_centroid, export_data.centroid.l_dmd));

    rmsd_percent.mean = struct( ...
        'e_pod', compute_rmsd_percent(dns_mean, export_data.mean.e_pod), ...
        'm_pod', compute_rmsd_percent(dns_mean, export_data.mean.m_pod), ...
        'l_pod', compute_rmsd_percent(dns_mean, export_data.mean.l_pod), ...
        'e_dmd', compute_rmsd_percent(dns_mean, export_data.mean.e_dmd), ...
        'm_dmd', compute_rmsd_percent(dns_mean, export_data.mean.m_dmd), ...
        'l_dmd', compute_rmsd_percent(dns_mean, export_data.mean.l_dmd));

    rmsd_definition = 'RMSD_percent = 100 * sqrt(mean((q_rec - q_dns).^2)) / sqrt(mean(q_dns.^2)).';

    export_dir = fullfile(fileparts(mfilename('fullpath')), 'exports');
    if ~exist(export_dir, 'dir')
        mkdir(export_dir);
    end

    export_file = fullfile(export_dir, sprintf('rise_velocity_analysis_Re%d_Bo%d.mat', Re, Bo));
    save(export_file, 'Re', 'Bo', 'time_nd', 'export_data', 'metadata', 'rmsd_percent', 'rmsd_definition', '-v7.3');

    fprintf('Saved rise-velocity export: %s\n', export_file);
end